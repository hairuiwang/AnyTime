#import "AnyTimePublic.h"

@implementation AnyTimePublic

+ (UIFont *)pingFangFontWithSize:(CGFloat)size {
    return [UIFont fontWithName:@"PingFangSC-Regular" size:size];
}

+ (UIFont *)futuraFontWithSize:(CGFloat)size {
    return [UIFont fontWithName:@"Futura" size:size];
}

+ (UIFont *)arialFontWithSize:(CGFloat)size {
    return [UIFont fontWithName:@"Arial" size:size];
}

@end
